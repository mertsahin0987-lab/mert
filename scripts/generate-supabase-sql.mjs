#!/usr/bin/env node
/**
 * Generates supabase/init.sql from the current CSV data.
 *
 * The output file contains:
 *   1. Schema — CREATE TABLE for brands, products, retailers,
 *      product_links, prices, price_history, notifications
 *   2. Row-Level Security policies (public read, service-role write)
 *   3. Seed data — INSERTs for everything in data/*.csv
 *
 * Usage:   npm run generate-supabase-sql
 * Then:    paste supabase/init.sql into the Supabase SQL Editor and run it
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const DATA_DIR = join(ROOT, 'data');
const OUT = join(ROOT, 'supabase', 'init.sql');

// === Tiny robust CSV parser (same as build-data.mjs) ===
function parseCsv(text) {
  const rows = [];
  let row = [], field = '', inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i], next = text[i + 1];
    if (inQuotes) {
      if (ch === '"' && next === '"') { field += '"'; i++; }
      else if (ch === '"') { inQuotes = false; }
      else { field += ch; }
    } else {
      if (ch === '"') inQuotes = true;
      else if (ch === ',') { row.push(field); field = ''; }
      else if (ch === '\n' || ch === '\r') {
        if (ch === '\r' && next === '\n') i++;
        row.push(field);
        if (row.length > 1 || row[0] !== '') rows.push(row);
        row = []; field = '';
      } else { field += ch; }
    }
  }
  if (field !== '' || row.length) { row.push(field); rows.push(row); }
  if (!rows.length) return [];
  const header = rows[0].map(h => h.trim());
  return rows.slice(1).map(r => {
    const o = {};
    header.forEach((h, i) => { o[h] = (r[i] ?? '').trim(); });
    return o;
  });
}

const readCsv = name => parseCsv(readFileSync(join(DATA_DIR, name), 'utf8'));

const sqlStr = v => {
  if (v === null || v === undefined || v === '') return 'NULL';
  return `'${String(v).replace(/'/g, "''")}'`;
};
const sqlNum = v => {
  if (v === null || v === undefined || v === '') return 'NULL';
  const n = parseFloat(v);
  return Number.isFinite(n) ? n.toString() : 'NULL';
};
const sqlBool = v => {
  const s = String(v).trim().toLowerCase();
  if (s === 'true' || s === 'yes' || s === '1') return 'TRUE';
  return 'FALSE';
};

// === Read all CSVs ===
const brands = readCsv('brands.csv');
const products = readCsv('products.csv');
const prices = readCsv('prices.csv');
const colors = readCsv('colors.csv');

// === Collect unique retailer names from prices ===
const retailerNames = [...new Set(prices.map(p => p.retailer).filter(Boolean))].sort();

// === Generate SQL ===
let sql = `-- =============================================================================
-- MySection — Supabase initialisation
-- =============================================================================
-- Generated by: scripts/generate-supabase-sql.mjs
-- Generated at: ${new Date().toISOString()}
--
-- Paste this whole file into the Supabase SQL Editor and hit Run.
-- Safe to re-run: drops and recreates everything.
-- =============================================================================

-- -----------------------------------------------------------------------------
-- Clean slate (only drops MySection tables — doesn't touch auth/storage/etc)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS price_history CASCADE;
DROP TABLE IF EXISTS prices         CASCADE;
DROP TABLE IF EXISTS product_links  CASCADE;
DROP TABLE IF EXISTS product_colors CASCADE;
DROP TABLE IF EXISTS products       CASCADE;
DROP TABLE IF EXISTS brands         CASCADE;
DROP TABLE IF EXISTS retailers      CASCADE;
DROP TABLE IF EXISTS notifications  CASCADE;

-- -----------------------------------------------------------------------------
-- Schema
-- -----------------------------------------------------------------------------

CREATE TABLE brands (
  id          TEXT PRIMARY KEY,
  name        TEXT NOT NULL UNIQUE,
  logo_key    TEXT,                      -- filename in assets/brands/
  logo_url    TEXT,                      -- optional: CDN URL
  created_at  TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE retailers (
  id            TEXT PRIMARY KEY,        -- slug, e.g. 'chris-and-sons'
  name          TEXT NOT NULL UNIQUE,    -- display name
  country       TEXT DEFAULT 'UK',
  base_url      TEXT,
  affiliate_net TEXT,                    -- 'amazon' | 'awin' | 'cj' | 'direct' | null
  created_at    TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE products (
  id                TEXT PRIMARY KEY,
  name              TEXT NOT NULL,
  brand_id          TEXT REFERENCES brands(id) ON DELETE SET NULL,
  category          TEXT NOT NULL,
  base_price        NUMERIC(10,2) NOT NULL,   -- used for sorting/filtering
  image_key         TEXT,                     -- filename in assets/products/
  image_url         TEXT,                     -- optional: CDN URL
  description       TEXT,
  is_new            BOOLEAN DEFAULT FALSE,
  trending          BOOLEAN DEFAULT FALSE,
  upcoming_release  BOOLEAN DEFAULT FALSE,
  release_date      DATE,
  created_at        TIMESTAMPTZ DEFAULT NOW(),
  updated_at        TIMESTAMPTZ DEFAULT NOW()
);
CREATE INDEX idx_products_brand    ON products(brand_id);
CREATE INDEX idx_products_category ON products(category);
CREATE INDEX idx_products_trending ON products(trending) WHERE trending = TRUE;
CREATE INDEX idx_products_is_new   ON products(is_new)   WHERE is_new   = TRUE;

CREATE TABLE product_colors (
  id                  BIGSERIAL PRIMARY KEY,
  product_id          TEXT REFERENCES products(id) ON DELETE CASCADE,
  name                TEXT NOT NULL,
  hex                 TEXT NOT NULL,
  image_key           TEXT,
  linked_product_id   TEXT REFERENCES products(id) ON DELETE SET NULL,
  sort_order          INT DEFAULT 0
);
CREATE INDEX idx_colors_product ON product_colors(product_id);

CREATE TABLE product_links (
  id              BIGSERIAL PRIMARY KEY,
  product_id      TEXT REFERENCES products(id) ON DELETE CASCADE,
  retailer_id     TEXT REFERENCES retailers(id) ON DELETE CASCADE,
  url             TEXT NOT NULL,         -- the retailer's product page (used by scraper)
  sku             TEXT,                  -- retailer's product ID if known
  last_scraped_at TIMESTAMPTZ,
  scrape_ok       BOOLEAN DEFAULT TRUE,
  UNIQUE (product_id, retailer_id)
);
CREATE INDEX idx_links_product  ON product_links(product_id);
CREATE INDEX idx_links_retailer ON product_links(retailer_id);

CREATE TABLE prices (
  id            BIGSERIAL PRIMARY KEY,
  product_id    TEXT REFERENCES products(id) ON DELETE CASCADE,
  retailer_id   TEXT REFERENCES retailers(id) ON DELETE CASCADE,
  price         NUMERIC(10,2) NOT NULL,
  currency      TEXT DEFAULT 'GBP',
  in_stock      BOOLEAN DEFAULT TRUE,
  url           TEXT,                    -- denormalised for fast reads
  fetched_at    TIMESTAMPTZ DEFAULT NOW(),
  last_seen_at  TIMESTAMPTZ,             -- last successful scrape
  last_checked_at TIMESTAMPTZ,           -- last attempted scrape (success or fail)
  last_error    TEXT,                    -- error message from last failed scrape
  UNIQUE (product_id, retailer_id)       -- one current price per pair
);
CREATE INDEX idx_prices_product ON prices(product_id);

CREATE TABLE price_history (
  id            BIGSERIAL PRIMARY KEY,
  product_id    TEXT REFERENCES products(id) ON DELETE CASCADE,
  retailer_id   TEXT REFERENCES retailers(id) ON DELETE CASCADE,
  price         NUMERIC(10,2) NOT NULL,
  currency      TEXT DEFAULT 'GBP',
  in_stock      BOOLEAN DEFAULT TRUE,
  seen_at       TIMESTAMPTZ DEFAULT NOW()
);
CREATE INDEX idx_history_product_time ON price_history(product_id, seen_at DESC);

CREATE TABLE notifications (
  id              TEXT PRIMARY KEY,
  title           TEXT NOT NULL,
  message         TEXT NOT NULL,
  type            TEXT NOT NULL,         -- 'release' | 'price_drop' | 'vat_free' | ...
  date            DATE NOT NULL,
  ends_at         TIMESTAMPTZ,
  retailer_id     TEXT REFERENCES retailers(id) ON DELETE SET NULL,
  discount_label  TEXT,
  urgent          BOOLEAN DEFAULT FALSE,
  created_at      TIMESTAMPTZ DEFAULT NOW()
);
CREATE INDEX idx_notifications_date ON notifications(date DESC);

-- -----------------------------------------------------------------------------
-- Row-Level Security
-- Everyone (incl. anon) can READ. Writes require the service_role key.
-- -----------------------------------------------------------------------------
ALTER TABLE brands          ENABLE ROW LEVEL SECURITY;
ALTER TABLE retailers       ENABLE ROW LEVEL SECURITY;
ALTER TABLE products        ENABLE ROW LEVEL SECURITY;
ALTER TABLE product_colors  ENABLE ROW LEVEL SECURITY;
ALTER TABLE product_links   ENABLE ROW LEVEL SECURITY;
ALTER TABLE prices          ENABLE ROW LEVEL SECURITY;
ALTER TABLE price_history   ENABLE ROW LEVEL SECURITY;
ALTER TABLE notifications   ENABLE ROW LEVEL SECURITY;

CREATE POLICY "public read brands"         ON brands         FOR SELECT USING (TRUE);
CREATE POLICY "public read retailers"      ON retailers      FOR SELECT USING (TRUE);
CREATE POLICY "public read products"       ON products       FOR SELECT USING (TRUE);
CREATE POLICY "public read product_colors" ON product_colors FOR SELECT USING (TRUE);
CREATE POLICY "public read product_links"  ON product_links  FOR SELECT USING (TRUE);
CREATE POLICY "public read prices"         ON prices         FOR SELECT USING (TRUE);
CREATE POLICY "public read price_history"  ON price_history  FOR SELECT USING (TRUE);
CREATE POLICY "public read notifications"  ON notifications  FOR SELECT USING (TRUE);

-- -----------------------------------------------------------------------------
-- Seed data (from data/*.csv)
-- -----------------------------------------------------------------------------

`;

// === Brands ===
sql += `-- Brands (${brands.length})\n`;
sql += `INSERT INTO brands (id, name, logo_key) VALUES\n`;
sql += brands
  .map(b => `  (${sqlStr(b.id)}, ${sqlStr(b.name)}, ${sqlStr(b.logo)})`)
  .join(',\n');
sql += `;\n\n`;

// === Retailers ===
sql += `-- Retailers (${retailerNames.length}, inferred from prices.csv)\n`;
sql += `INSERT INTO retailers (id, name, country) VALUES\n`;
sql += retailerNames
  .map(name => {
    const id = name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
    return `  (${sqlStr(id)}, ${sqlStr(name)}, 'UK')`;
  })
  .join(',\n');
sql += `;\n\n`;

// Build lookup: retailer name → id
const retailerId = name => name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
const brandId = name => {
  const b = brands.find(br => br.name === name);
  return b ? b.id : null;
};

// === Products ===
sql += `-- Products (${products.length})\n`;
sql += `INSERT INTO products (id, name, brand_id, category, base_price, image_key, description, is_new, trending, upcoming_release, release_date) VALUES\n`;
sql += products
  .map(p => {
    const bid = brandId(p.brand);
    return `  (${sqlStr(p.id)}, ${sqlStr(p.name)}, ${sqlStr(bid)}, ${sqlStr(p.category)}, ${sqlNum(p.price)}, ${sqlStr(p.image)}, ${sqlStr(p.description)}, ${sqlBool(p.isNew)}, ${sqlBool(p.trending)}, ${sqlBool(p.upcomingRelease)}, ${p.releaseDate ? sqlStr(p.releaseDate) : 'NULL'})`;
  })
  .join(',\n');
sql += `;\n\n`;

// === Colors ===
if (colors.length) {
  sql += `-- Product colours (${colors.length})\n`;
  sql += `INSERT INTO product_colors (product_id, name, hex, image_key, linked_product_id, sort_order) VALUES\n`;
  sql += colors
    .map((c, i) => `  (${sqlStr(c.productId)}, ${sqlStr(c.name)}, ${sqlStr(c.hex)}, ${sqlStr(c.image)}, ${sqlStr(c.linkedProductId)}, ${i})`)
    .join(',\n');
  sql += `;\n\n`;
}

// === Product links + current prices (same source data) ===
sql += `-- Product links — where each retailer sells each product (${prices.length})\n`;
sql += `INSERT INTO product_links (product_id, retailer_id, url) VALUES\n`;
sql += prices
  .map(p => `  (${sqlStr(p.productId)}, ${sqlStr(retailerId(p.retailer))}, ${sqlStr(p.url)})`)
  .join(',\n');
sql += `\nON CONFLICT (product_id, retailer_id) DO UPDATE SET url = EXCLUDED.url;\n\n`;

sql += `-- Current prices (${prices.length})\n`;
sql += `INSERT INTO prices (product_id, retailer_id, price, in_stock, url) VALUES\n`;
sql += prices
  .map(p => `  (${sqlStr(p.productId)}, ${sqlStr(retailerId(p.retailer))}, ${sqlNum(p.price)}, ${sqlBool(p.inStock)}, ${sqlStr(p.url)})`)
  .join(',\n');
sql += `\nON CONFLICT (product_id, retailer_id) DO UPDATE SET
  price = EXCLUDED.price,
  in_stock = EXCLUDED.in_stock,
  url = EXCLUDED.url,
  fetched_at = NOW();\n\n`;

sql += `-- =============================================================================
-- Done. Verify in the Supabase dashboard:
--   Table Editor → brands (${brands.length}), products (${products.length}), prices (${prices.length})
-- =============================================================================
`;

writeFileSync(OUT, sql);
console.log(`✓ Wrote ${OUT.replace(ROOT + '/', '')}`);
console.log(`  ${brands.length} brands · ${products.length} products · ${prices.length} prices · ${colors.length} colours · ${retailerNames.length} retailers`);
console.log(`  ${sql.length.toLocaleString()} characters of SQL`);
